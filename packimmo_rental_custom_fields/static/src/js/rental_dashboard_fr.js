function translateText(value) {
    const trimmed = value.trim();
    if (!trimmed) {
        return value;
    }

    let translated = TRANSLATIONS[trimmed];
    if (!translated) {
        translated = trimmed
            .replace(/^(\d+) new$/, "$1 nouveau(x)")
            .replace(/^(\d+) active$/, "$1 actif(s)")
            .replace(/^(\d+) total$/, "$1 au total")
            .replace(/^(\d+) mapped$/, "$1 cartographié(s)")
            .replace(/^(\d+) this period$/, "$1 sur cette période")
            .replace(/^(\d+) units$/, "$1 unité(s)")
            .replace(/^(\d+) invoices$/, "$1 facture(s)")
            .replace(/^(\d+) in last 30d$/, "$1 au cours des 30 derniers jours")
            .replace(/(\d+)d left/g, "$1 j restant(s)")
            .replace(/\bMonday\b/g, "lundi")
            .replace(/\bTuesday\b/g, "mardi")
            .replace(/\bWednesday\b/g, "mercredi")
            .replace(/\bThursday\b/g, "jeudi")
            .replace(/\bFriday\b/g, "vendredi")
            .replace(/\bSaturday\b/g, "samedi")
            .replace(/\bSunday\b/g, "dimanche")
            .replace(/\bJanuary\b/g, "janvier")
            .replace(/\bFebruary\b/g, "février")
            .replace(/\bMarch\b/g, "mars")
            .replace(/\bApril\b/g, "avril")
            .replace(/\bMay\b/g, "mai")
            .replace(/\bJune\b/g, "juin")
            .replace(/\bJuly\b/g, "juillet")
            .replace(/\bAugust\b/g, "août")
            .replace(/\bSeptember\b/g, "septembre")
            .replace(/\bOctober\b/g, "octobre")
            .replace(/\bNovember\b/g, "novembre")
            .replace(/\bDecember\b/g, "décembre");
    }

    if (translated === trimmed) {
        return value;
    }
    return value.replace(trimmed, translated);
}

function translateNode(node) {
    if (node.nodeType === Node.TEXT_NODE) {
        node.nodeValue = translateText(node.nodeValue);
        return;
    }
    if (node.nodeType !== Node.ELEMENT_NODE) {
        return;
    }

    for (const attribute of ["title", "aria-label", "placeholder"]) {
        if (node.hasAttribute(attribute)) {
            node.setAttribute(attribute, translateText(node.getAttribute(attribute)));
        }
    }

    node.childNodes.forEach(translateNode);
}

function translateDashboard(root) {
    translateNode(root);

    const pageTitle = root.querySelector(".rm-ht h1");
    const activeNav = root.querySelector(".rm-nav-item.rm-nav-active");
    if (pageTitle && activeNav) {
        const page = Object.keys(PAGE_TITLES).find((key) => activeNav.classList.contains(`rm-nav-${key}`));
        if (page) {
            pageTitle.textContent = PAGE_TITLES[page];
        }
    }
}

function startDashboardTranslation() {
    const translateAll = () => {
        document.querySelectorAll("[data-packimmo-dashboard-fr='1']").forEach(translateDashboard);
    };
    let scheduled = false;
    const scheduleTranslation = () => {
        if (scheduled) {
            return;
        }
        scheduled = true;
        requestAnimationFrame(() => {
            scheduled = false;
            translateAll();
        });
    };

    translateAll();
    new MutationObserver(scheduleTranslation).observe(document.body, {
        childList: true,
        characterData: true,
        subtree: true,
    });
}

if (document.readyState === "loading") {
    document.addEventListener("DOMContentLoaded", startDashboardTranslation, {once: true});
} else {
    startDashboardTranslation();
}
